def reverse(llist):
    # Write your code here
    stack = []
    current = llist
    while current is not None:
        stack.append(current)
        current = current.next
    # If head is null (list empty: len(stack) is 0)
    if len(stack) == 0 or len(stack) == 1:
        return llist
    # If not, pop first two
    prev = stack.pop()
    head = prev
    now = stack.pop()
    prev.next = now
    now.prev = prev
    while len(stack) > 0:
        prev = now
        now = stack.pop()
        prev.next = now
        now.prev = prev
    # At the end, append none to now
    now.next = None
    return head
    

